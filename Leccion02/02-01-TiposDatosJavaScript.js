/*
Ejemplos de 
tipos de datos 
en JavaScript
*/

//Tipo de dato string
var nombre = "Carlos";
console.log(typeof nombre);
nombre = 10.5;
//Saber que tipo de dato es (typeof)
console.log(typeof nombre)

//Tipo de dato numerico
var numero = 1000;
console.log(numero);

//Tipo de dato object
var objeto = {
    nombre : "Juan",
    apellido : "Perez",
    telefono : "55443322"
}
console.log(typeof objeto);

//Tipo de dato boolean (true, false)
//Se llaman bandera
var bandera = false;
console.log(bandera);
console.log(typeof bandera)